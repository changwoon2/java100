public class operator {
    public static void main(String[] args) {
        int a = 0, b = 100;
        a = a + 1;
        System.out.println(a);

        a += 1;
        System.out.println(a);

        a -= 1;
        System.out.println(a);

        b++;
        System.out.println(b);
        b++;
        System.out.println(b);
        b--;
        System.out.println(b);
    }
}
